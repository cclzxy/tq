package com.example.hx_test.demo.Pojo;


public class Points {

  private String pid;
  private long classid;
  private long itemid;
  private double itemscore;
  private java.sql.Date dates;
  private String details;
  private long noon;
  private long aftern;


  public String getPid() {
    return pid;
  }

  public void setPid(String pid) {
    this.pid = pid;
  }


  public long getClassid() {
    return classid;
  }

  public void setClassid(long classid) {
    this.classid = classid;
  }


  public long getItemid() {
    return itemid;
  }

  public void setItemid(long itemid) {
    this.itemid = itemid;
  }


  public double getItemscore() {
    return itemscore;
  }

  public void setItemscore(double itemscore) {
    this.itemscore = itemscore;
  }


  public java.sql.Date getDates() {
    return dates;
  }

  public void setDates(java.sql.Date dates) {
    this.dates = dates;
  }


  public String getDetails() {
    return details;
  }

  public void setDetails(String details) {
    this.details = details;
  }


  public long getNoon() {
    return noon;
  }

  public void setNoon(long noon) {
    this.noon = noon;
  }


  public long getAftern() {
    return aftern;
  }

  public void setAftern(long aftern) {
    this.aftern = aftern;
  }

}
